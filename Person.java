public class Person {
    private int age;
    private String name;
    private char gen;
    
    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public char getGen() {
        return gen;
    }

    public Person(int age, String name, char gen) {
        this.age = age;
        this.name = name;
        this.gen = gen;
    }

}
