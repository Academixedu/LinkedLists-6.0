import java.util.LinkedList;


public class ForL{
  public static void main(String[]args){
  // create a Linked List Object
    // Add Values
    // iterate it by using For Each
    
    LinkedList<Integer> list=new LinkedList<>();

    list.add(1);
    list.add(2);    
    list.add(3);
    list.add(4);
    list.add(5);
    list.add(6);
    list.add(7);
    
    for(Integer i:list){
      System.out.println(i);
    }



  }}
