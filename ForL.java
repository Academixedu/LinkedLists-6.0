import java.util.LinkedList;

public class ForL{
  public static void main(String[]args){
  // create a Linked List Object
    // Add Values
    // iterate it by using For Each
    LinkedList<Integer> s=new LinkedList<>();

  
    s.add(2);
    s.add(5);
    s.add(10);
    for(int i :s){
      System.out.println(i);
  }}
}
