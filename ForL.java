import java.util.LinkedList;

public class ForL{
  public static void main(String[]args){
  // create a Linked List Object
    // Add Values
    // iterate it by using For Each
    LinkedList<Integer> z=new LinkedList<>();
    z.add(1);
    z.add(2);
    z.add(3);
    z.add(5);
    z.add(7);
    for(Integer i:z){
      System.out.println(i);
    }
  }}
